package org.wmn4j.notation;

/**
 * Articulations that affect a single note.
 */
public enum Articulation {
	/**
	 * Specifies an accent marking.
	 */
	ACCENT,

	/**
	 * Specifies a staccato marking.
	 */
	STACCATO,

	/**
	 * Specifies a tenuto marking.
	 */
	TENUTO,

	/**
	 * Specifies a fermata.
	 */
	FERMATA
}
